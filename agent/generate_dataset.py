def main():
    out_dir = Path("/home/z/my-project/download/composio-research-agent/results")
    out_dir.mkdir(parents=True, exist_ok=True)

    records = []
    for tup in APPS:
        (id_, name, cat, ol, auth, ss, gr, api, ab, mcp, build, block, url, notes) = tup
        records.append({
            "id": id_,
            "name": name,
            "category": cat,
            "one_liner": ol,
            "auth_methods": auth,
            "self_serve": ss,
            "gating_reason": gr,
            "api_surface": api,
            "api_breadth": ab,
            "has_mcp": mcp,
            "buildability": build,
            "main_blocker": block,
            "evidence_url": url,
            "notes": notes,
            "verified": False,
            "verification_status": "unverified",
        })

    out_path = out_dir / "all_apps.json"
    out_path.write_text(json.dumps(records, indent=2))
    print(f"[done] {len(records)} records → {out_path}")

    # Quick analytics
    from collections import Counter
    print("\n--- Pattern preview ---")
    print("Auth (primary):", Counter(r["auth_methods"][0] for r in records).most_common())
    print("Self-serve   :", Counter(r["self_serve"] for r in records).most_common())
    print("Has MCP      :", Counter(r["has_mcp"] for r in records).most_common())
    print("Buildability :", Counter(r["buildability"] for r in records).most_common())


if __name__ == "__main__":
    main()
